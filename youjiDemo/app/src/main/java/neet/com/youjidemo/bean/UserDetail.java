package neet.com.youjidemo.bean;

public class UserDetail {
    private String userTouxiangUrl;
    private String userIntroduction;
    private String username;
    private String userBirthday;
    private String userSex;
    private String userHometown;

    public String getUserTouxiangUrl() {
        return userTouxiangUrl;
    }

    public void setUserTouxiangUrl(String userTouxiangUrl) {
        this.userTouxiangUrl = userTouxiangUrl;
    }

    public String getUserIntroduction() {
        return userIntroduction;
    }

    public void setUserIntroduction(String userIntroduction) {
        this.userIntroduction = userIntroduction;
    }

    public String getUsername() {
        return username;
    }

    public void setUsername(String username) {
        this.username = username;
    }

    public String getUserBirthday() {
        return userBirthday;
    }

    public void setUserBirthday(String userBirthday) {
        this.userBirthday = userBirthday;
    }

    public String getUserSex() {
        return userSex;
    }

    public void setUserSex(String userSex) {
        this.userSex = userSex;
    }

    public String getUserHometown() {
        return userHometown;
    }

    public void setUserHometown(String userHometown) {
        this.userHometown = userHometown;
    }
}
